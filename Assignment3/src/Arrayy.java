
public class Arrayy {

	public static void main(String[] args) {
		String[][] s=new String[][]{{"India","a1","a2","a3","a4","a5"},{"Us","b1","b2","b3","b4","b5"},{"Pakistan","c1","c2","c3","c4","c5"},{"Uk","d1","d2","d3","d4","d5"},{"Africa","e1","e2","e3","e4","e5"}};
		
			
			
			
		
		System.out.println("entries are shown below ");
			
	for (int i=0;i<s.length;i++){
		for (int j=0;j<s[0].length;j++){
			System.out.print(s[i][j]+" ");
			
			
		}
		System.out.println();
	}
		
}}
